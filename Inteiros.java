/*Imprima os valores inteiros entre 10 e 100*/

public class Inteiros {
    public static void main(String[] args) {
        /* Criação da variável que irá receber os valores */
        int i;
        /*
         * O i começa com 10 e enquanto ele for menor ou igual a 100, i irá adicionar +1
         * na contagem.
         */
        for (i = 10; i <= 100; i++) {
            System.out.println("Contagem " + i);
        }

    }

}
